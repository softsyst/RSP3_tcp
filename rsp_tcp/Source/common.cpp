#ifdef _WIN32
int common::gettimeofday(struct timeval *tv, void* ignored)
{
	FILETIME ft;
	unsigned __int64 tmp = 0;
	if (NULL != tv) {
		GetSystemTimeAsFileTime(&ft);
		tmp |= ft.dwHighDateTime;
		tmp <<= 32;
		tmp |= ft.dwLowDateTime;
		tmp /= 10;
#ifdef _MSC_VER
		tmp -= 11644473600000000Ui64;
#else
		tmp -= 11644473600000000ULL;
#endif
		tv->tv_sec = (long)(tmp / 1000000UL);
		tv->tv_usec = (long)(tmp % 1000000UL);
	}
	return 0;
}
#endif

	template<typename Out>
	void common::split(const std::string &s, char delim, Out result) {
		std::stringstream ss(s);
		std::string item;
		while (std::getline(ss, item, delim)) {
			*(result++) = item;
		}
	}

	std::vector<std::string> common::split(const std::string &s, char delim) {
		std::vector<std::string> elems;
		split(s, delim, std::back_inserter(elems));
		return elems;
	}

	bool common::checkRange(int val, int minval, int maxval)
	{
		if (val >= minval && val <= maxval)
			return true;
		return false;
	}

	bool common::isLittleEndian()
	{
		if (htonl(99) == 99)
			return false;
		return true;
	}

	timespec common::getRelativeTimeoutValue(int relativeTimeoutSec)
	{
		struct timeval now;
		gettimeofday(&now, NULL);
		struct timespec timeout;//1 second
		timeout.tv_sec = now.tv_sec + relativeTimeoutSec;
		timeout.tv_nsec = now.tv_usec * 1000;
		return timeout;
	}




